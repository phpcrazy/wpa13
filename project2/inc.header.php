<p class="title">about</p>
<p class="title">history</p>

<p>This website was founded in February 2014.
Initially both a gallery space/store and a global online destination, 
oki-ni was created to work with established brands and progressive designers, 
to offer innovative products to knowledgeable fashion consumers.</p>

<p>Exclusively limited-edition, oki-ni also collaborated with brands to produce unique pieces. 
Early collaborators included Adidas, Levi's, Paul Smith and Porter.</p>


<p class="title">today</p>
<p>As years have passed oki-ni's horizons have broadened, while the ethos of the brand has remained the same - namely, 
to discover and break the most inspiring brands and products available, while hosting them in an environment that is rich, 
inspiring and innovative.</p>

<p>oki-ni is a group of young and enthused, product-focused individuals, 
led by three directors with over 50 years of combined experience in high-end menswear retailing.</p>

<p class="title">future</p>
<p>On-going projects such as STYLED, the MIX SERIES, 
our FEATURES section and seasonal VIDEO content ensure that the oki-ni site is a vibrant and forward-thinking destination 
for the future.</p> 

